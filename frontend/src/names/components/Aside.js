const NestedGrid = (props) => {
  const classes = useStyles();

  const FormRow = (props) => {
    const colItems = props.rowNames.map((name) => (
      <Grid item>
        <Paper className={props.classes.paper}>{name}</Paper>
      </Grid>
    ));
    return <React.Fragment>{colItems}</React.Fragment>;
  };

  function splitArray(flatArray, numCols) {
    const newArray = [];
    for (let c = 0; c < numCols; c++) {
      newArray.push([]);
    }
    for (let i = 0; i < flatArray.length; i++) {
      const mod = i % numCols;
      newArray[mod].push(flatArray[i]);
    }
    return newArray;
  }
  const nameLists = splitArray(props.names, 4);

  const colItems = nameLists.map((names) => (
    <Grid>
      <FormRow rowNames={names} classes={classes} />
    </Grid>
  ));
  return (
    <div className={classes.root}>
      <Grid container spacing={1}>
        {colItems}
      </Grid>
    </div>
  );
};

